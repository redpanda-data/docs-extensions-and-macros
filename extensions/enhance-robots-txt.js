'use strict';

/**
 * Enhances Antora's default robots.txt with AI-friendly crawler permissions.
 *
 * Antora generates a basic robots.txt based on the `robots:` setting in the playbook:
 * - `robots: allow` generates: User-agent: *\nAllow: /
 * - `robots: disallow` generates: User-agent: *\nDisallow: /
 *
 * This extension enhances the allow case by adding:
 * - Explicit permissions for AI crawlers (GPTBot, Claude-Web, Perplexity, etc.)
 * - Sitemap reference
 * - Crawl-delay directive
 *
 * The extension runs after Antora generates robots.txt and enhances it for better AI discoverability.
 */
module.exports.register = function () {
  const logger = this.getLogger('enhance-robots-txt-extension');

  this.on('beforePublish', ({ contentCatalog, siteCatalog }) => {
    // Find the robots.txt file generated by Antora
    const robotsFile = siteCatalog.getFiles().find(file =>
      file.out && file.out.path === 'robots.txt'
    );

    if (!robotsFile) {
      logger.warn('No robots.txt found - ensure robots setting is configured in playbook');
      return;
    }

    const currentContent = robotsFile.contents.toString();

    // Only enhance if it's the "allow" version
    if (!currentContent.includes('Allow: /')) {
      logger.info('robots.txt contains Disallow - not enhancing (likely preview build)');
      return;
    }

    const siteUrl = contentCatalog.getComponent('ROOT')?.url ||
                    siteCatalog.getFiles().find(f => f.pub?.url)?.pub?.url?.split('/')[0] ||
                    'https://docs.redpanda.com';

    const enhancedContent = `# Redpanda Documentation - AI-Friendly Robots Configuration
# This configuration explicitly welcomes AI crawlers and search engines

User-agent: *
Allow: /

# Explicitly allow common AI crawlers

# OpenAI GPT
User-agent: GPTBot
Allow: /

User-agent: ChatGPT-User
Allow: /

# Anthropic Claude
User-agent: Claude-Web
Allow: /

User-agent: anthropic-ai
Allow: /

# Perplexity
User-agent: Perplexity
Allow: /

User-agent: PerplexityBot
Allow: /

# Google AI
User-agent: Google-Extended
Allow: /

User-agent: GoogleOther
Allow: /

# Common Crawl (used by many AI systems)
User-agent: CCBot
Allow: /

# Additional AI platforms
User-agent: cohere-ai
Allow: /

User-agent: Omgilibot
Allow: /

User-agent: FacebookBot
Allow: /

# Sitemap for discovery
Sitemap: ${siteUrl}/sitemap.xml

# Crawl delay (be respectful of server resources)
Crawl-delay: 1
`;

    robotsFile.contents = Buffer.from(enhancedContent, 'utf8');
    logger.info('Enhanced robots.txt with AI-friendly crawler permissions');
  });
};
